package sample1;

public class Arithmetic {
	public static void main (String[]args) {
	
		int a = 5;
		a++;
		int b = 2;
		b--;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		System.out.println(a);
		System.out.println(b);
		
	}

}
